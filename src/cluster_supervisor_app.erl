%%file_comment
-module(cluster_supervisor_app).

-behaviour(application).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% Behavioural exports
%% --------------------------------------------------------------------
-export([
	 start/2,
	 stop/1,
	 run/0
        ]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% API Functions
%% --------------------------------------------------------------------

run() ->
	appmon:start(),
	mnesia:start(),
	application:load(cluster_supervisor),
	application:start(cluster_supervisor).

%% application:set_env(cluster_supervisor,callbacks,CallBacks),
	

%% ====================================================================!
%% External functions
%% ====================================================================!
%% --------------------------------------------------------------------
%% Func: start/2
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}
%% --------------------------------------------------------------------
start(_Type, StartArgs) ->
	case mnesia:wait_for_tables(mnesia:system_info(local_tables),60000) of
		ok ->
    		case cluster_supervisor_sup:start_link(StartArgs) of
				{ok, Pid} ->
	    			{ok, Pid};
				Error ->
	    			Error
    			end;
		MnesiaError ->
			error_logger:error_msg("Mnesia did not become ready in ~p:start()!~n~p~n",[?MODULE,MnesiaError]),
			MnesiaError
	end.

%% --------------------------------------------------------------------
%% Func: stop/1
%% Returns: any
%% --------------------------------------------------------------------
stop(_State) ->
	cluster_vip_manager:stop_local_vips(),
    ok.

